var inputArray = [];
async function InputArray() {
  console.log("InputArray()");
  var element = prompt("Enter number of elements:");
  for (var i = 0; i < element; i++) {
    inputArray[i] = prompt(`Enter value ${i+1}:`);
  }
  for (var i = 0; i <= element; i++) {
    console.log(inputArray[i]);
  }
}

// inputArray=[20, 15, 60, 200, 150, 100, 50, 90, 180, 160, 120, 140,88,56,23,10,46,90];

const InpArrbtn = document.querySelector(".InputArray");
InpArrbtn.addEventListener("click", async function () {
  disableSortingBtn();
  disableSizeSlider();
  disableNewArrayBtn();
  await InputArray();
  enableSortingBtn();
  enableSizeSlider();
  enableNewArrayBtn();
});

export default inputArray;